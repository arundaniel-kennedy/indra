      <h2 class="mb-5">About Us</h2>

        <p>INDRA (INDIAN NATIONAL DEVELOPMENT REFOMATION ASSOCIATION) TRUST, with the motto "Service before self" is Non-Governmental Organisation that works to render the dream of our former President honourable Dr. A.P.J. ABDUL KALAM, INDIA 2020 A Vision of The New Millennium.
            <br />
            <br /> INDRA’s work isn’t concerned to a spot or a particular issue it focuses on growth in vast field, and over all INDIA. It functions in 4 different wings namely Green Wing, Techo Wing, Service Wing and Social media Wing which represents the key arenas of focus such as Environmental stability, financial development, Education, Livelihood, Technological improvement and etc. and provides platform to showcase your talent towards the development of the nation.
            <br />
            <br /> INDRA specifies on the youth and children, on their education and push towards the development of them and the country. INDRA believes growth of the individual leads to the growth of the country. INDRA wants all the children to be educated – a high quality education, in spite of their social status, caste and religion, which eradicate poverty. INDRA aims on to provide all the basic necessity to each of the citizen.
            <br />
            <br /> INDRA further works to improve the nation concentrating on climate change, global warming, water scarcity, and pollution by organizing tree plantation, lake clearance and such needy services, and also creates awareness by camping.
            <br />
            <br /> INDRA’s network extends to all the nook and corner of INDIA.
            <br />
            <br /> INDRA is totally an unprofitable, service oriented, Non-Governmental Organization with INDIA’s Development its main and only motive.
            <br />
            <br /> INDRA is a nest parented by the Chief, with volunteers from NCC, NSS, Scout and service-minded souls.
            <br />
            <br /> INDRA started its service from June 2005, organized officially in 2018 and registered in 2019, and focuses on INDIA 2040 – a developed country. It works by establishing 5 year plans, and sincerely works with it, to meet its target within time. This has been more a family than an organization and has a big home INDIA.
            <br />
            <br /> INDRA targets more on solution than on situations, to achieve its motive. INDRA took up the responsibility, set a goal, laid a plan, remembering its purpose, is fighting the battle, all it needs is your helping hand. Today we strive to fulfil the dream of the great dreamer, Dr. APJ Abdul Kalam. To make India a developed nation. It sounds farfetched and difficult. But we have in the lead a man who made the impossible, possible and with him, a group of young people burning with determination and we are sure that their light is going to shine bright and brilliant one day, illuminating the face of our country, our India.
            <br />
            <br />


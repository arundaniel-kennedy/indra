            <br /> Our trust is held together at the national level by the President followed by the Vice President who will be the Chief Executive Officer working extensively with the Principal Directors. The Principal Directors head the 5 zones of INDRA, namely, the North Zone, South Zone, East Zone, West Zone and Central Zone, and are abetted by the Directors who represent our trust at the state level. They are further assisted by Deputy Directors who command the 4 wings of INDRA - the Green Wing, Technical and Industrial Wing, Service Wing and Social Media Wing. To strengthen our trust further, we have Joint Directors at the district level, followed by Secretaries to supervise at the city/town/village level. Last, but not the least, we have our Members who are the actual workforce of our trust. They help us reach the grassroots level of any problem and resolve it.
